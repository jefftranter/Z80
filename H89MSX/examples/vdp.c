void main(void)
{
    uint16_t i;
    uint8_t ch;

    vdp_write_reg(1, 0x00);

    vdp_write_reg(0, 0x00);
    vdp_write_reg(1, 0x10);
    vdp_write_reg(2, NAME_TABLE >> 10);
    vdp_write_reg(4, PATTERN_TABLE >> 11);
    vdp_write_reg(7, 0xF4);

    download_font();

    vdp_set_write_address(NAME_TABLE);

    ch = 'A';

    for (i = 0; i < 960; i++)
    {
        vdp_write(ch);
        ch++;
        if (ch > 'Z')
            ch = 'A';
    }

    vdp_write_reg(1, 0x50);

    printf("Got here\n");

    while (1);
}
